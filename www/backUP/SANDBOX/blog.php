          <h2>The cost of a PO Box</h2>
           2nd of May 2012
      </div>
      <div class="single-entry-text">             
      <p>A friend of mine who is into marketing once told me that Canada Post is one of the most valuable brands in existence.  By that he meant not the service it provides but the goodwill that the name holds with its users who view it as a trustworthy and efficient service.  Personally I like Canada Post.  I prefer it to companies like UPS, DHL and TNT who principally provide the maddening service of posting those sticky notices telling you that you can't have your package because you- as you were already aware- were not at home.  Isn't that what a postal box is for?  Canada Post has never had trouble delivering or holding on to a package for weeks at a nearby postal outlet- but many of the big courier companies have actually returned packages without my consent because it was not convenient for them to deliver it at a time when I could sign for it.</p>
      <p>According to a Boston.com article, it seems that the US postal service is in a bit of <a href="http://articles.boston.com/2012-04-17/business/31356274_1_mail-volume-postal-service-first-class-mail-delivery">trouble</a> lately due to online communication and bills.</p>
      <p>I hope that eSnail.ca is on the side of Canada Post, rather than being one of those companies that are slowly causing it to go the way of the Cotton Gin.  Perhaps a day like today is  good one to reaffirm that Canada Post is eSnail.ca's first choice when it comes to forwarding packages and letters.  We do this not because it is the most cost-effective option, (though it often is) but because it is the most convenient for our users and because Canada Post offers a high level of service for rural and remote addresses. Canada Post is a company with vision who puts service above convenience and profit.  That's exactly the kind of company eSnail.ca hopes to be. </p>
      <p>Have a great day,</p>
