<div class="content-box faq-page-box" id="q13">
	<p class="question">Doesn't my credit card billing address need to be the same as my shipping address for online purchases?</p>
    <p class="answer">For security reasons many Credit card companies do require that you register both your billing and mailing addresses with them before you buy things online. However you can ask your credit card company to add multiple addresses to your account so that you can recieve your bills to one address and have purchases mailed to another address.</p>
</div>

<div class="content-box faq-page-box" id="q14">
	<p class="question">What measures are in place to ensure that my mail and personal information is secure?</p>
    <p class="answer">Currently all personal data is stored offline under AES 256-bit hardware-encryption. All correspondence is emailed in an encryped format and all of our employess must adhere to strict privacy guidelines. </p>
