          <h2>World's Highest Paid Letter Opener</h2>
           28th of April 2012
      </div>
      <div class="single-entry-text">             
      <p>It's nice to see positive change happening in the world.  A friend of mine sent me a link to <a href="http://www.post.ch/en/post-startseite/post-swisspostbox.htm">this</a> company in Switzerland which has begun to offer scanning and storage online.  Unfortunately they are running the business on the same principle as Earth Class Mail, an Oregon based startup that offers a similar service as eSnail.ca. Basically their service involves scanning the envelope- ONLY the envelope- and then asking the user to pay $1.50 just to send you a scanned copy of the envelope's actual contents.</p>
      <p>While we wish our friends at Swiss Post and Earth Class Mail the best it seems to us rather silly to make our users wait a full day just to see the contents of their mail- let alone to charge $1.50 for us to use a letter opener.   I can't speak for everyone, but I open every piece of mail with my name on it- which still makes up the bulk of the mail I personally receive.  If I kept an account at Earth Class Mail, It would drive me batty wondering what's in the envelope each time I received and wondering if it was worth it to pay the $1.50 to open it.</p>
      <p>The only thing worse than having a frustrated customer is being a frustrated customer.  So let us know how we're doing.</p>
      <p>Until next entry, have a great day.</p>
