<div class="content-box faq-page-box" id="q01">
	<p class="question">Can I receive magazines by eSnail?</p>
    <p class="answer">Yes! If you would like to subscribe to a magazine using your eSnail Box we will notify you when you have mail and either forward it to you for a $5 forwarding fee, or colour scan it for you for only $1.50 Please note that scanning the magazine will destroy the bindings.</p>
</div>

<div class="content-box faq-page-box" id="q02">
	<p class="question">Can I receive cheques that are mailed to my eSnail address?</p>
    <p class="answer">Yes! When we receive a cheque from you we will send you an email confirming your address. Once you confirm your address we will mail you the cheque directly. Alternatively we can mail you the cheque immediately if you select the "I would like my cheques mailed to this address" option when you register.</p>
</div>

<div class="content-box faq-page-box" id="q03">
	<p class="question">What if I move or travel?</p>
    <p class="answer">You receive mail addressed to your eSnail account wherever you have access to the internet. Your account goes wherever you go!</p>
</div>

<div class="content-box faq-page-box" id="q04">
	<p class="question">Will you send me any junkmail?</p>
    <p class="answer">Because our scanning process is automated, we will scan and email all mail that is addressed under the name you registered with eSnail.ca This may include fliers and lettermail.</p>
</div>

<div class="content-box faq-page-box" id="q05">
	<p class="question">Do I have to download any special software?</p>
    <p class="answer">All you need to open your mail is an email account and browser.</p>
</div>

<div class="content-box faq-page-box" id="q06">
	<p class="question">What happens to my mail after it's scanned?</p>
    <p class="answer">After we've scanned your mail it is immediately stored in a secure storage facility. We hold your mail there for 90 days in case you would like to have a physical copy of your mail forwarded to you. After 90 days it will be automatically shredded and recycled to protect your privacy and the environment.</p>
</div>

<div class="content-box faq-page-box" id="q07">
	<p class="question">Can I have my mail forwarded to me?</p>
    <p class="answer">Absoloutely! We understand that you might be recieving important documents or letters with sentinmental value so we'll honor any request for physical copies.</p>
</div>

<div class="content-box faq-page-box" id="q08">
	<p class="question">What happens if I get a parcel shipped to this address?</p>
    <p class="answer">We'd be happy to forward you any parcels you receive at this address to wherever you are in the world, however a handling fee of $5 plus postage will apply.</p>
</div>

<div class="content-box faq-page-box" id="q09">
	<p class="question">What happens if I go over my monthly limit?</p>
    <p class="answer">If your account goes over limit, we will first noitify you. However if you go over your limit and decide not to upgrade your plan we will charge you $0.25 for each additional piece of mail to receive.</p>
</div>

<div class="content-box faq-page-box" id="q10">
	<p class="question">How do I modify my account settings or change my service?</p>
    <p class="answer">If you would like to modify your account, simply re-register under the new account type of your choice. the billing and service changes will appear in your next statement. If you would like to request features such as OCR be added to your account, please use the contact form on the "contact" page.</p>
</div>

<div class="content-box faq-page-box" id="q11">
	<p class="question">What happens if a catalog subscription gets shipped to eSnail?</p>
    <p class="answer">Catalogs that are adressed to you will be treated the same as magazines and we will give you the option of scanning, recycling or forwarding them to your current address.</p>
</div>

