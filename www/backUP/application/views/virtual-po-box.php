<p>A post-office box or Post Office box (commonly referred to as a PO Box or a Postal Box) is, according to Wikipedia, a uniquely addressable lockable box located on the premises of a post office station.  They are commonly used as an alternative to door-to-door delivery.  There are however many other why renting an PO box is a good idea for individuals and businesses.</p>

